/***************************************************************************************************
 * @fn      HalBtCtrl
 *
 * @brief   
 *
 * @param   
 *          
 * @return  None
 ***************************************************************************************************/
void HalBtCtrl( uint8_t dev, uint8_t ctrl )
{
    uint8_t pb[2];
    pb[0] = dev;
    pb[1] = ctrl;
    
    HalUiBrdRegWrite( HAL_UIBRD_REG_BT_CTRL, pb, sizeof(pb) );
}

